int		ft_str_is_uppercase(char *str)
{
	int i;

	i = 0;
	while (*(str + i) != '\0')
	{
		if (*(str + i) < 'A')
			return (0);
		else if (*(str + i) > 'Z')
			return (0);
		i++;
	}
	return (1);
}
