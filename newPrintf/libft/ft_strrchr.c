#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	size_t	slen;

	slen = 0;
	while (s[slen])
		slen++;
	if (c == '\0')
		return ((char *)(s + slen));
	i = slen - 1;
	while (i >= 0)
	{
		if (s[i] == c)
			return ((char *)(s + i));
		i--;
	}
	return (NULL);
}
