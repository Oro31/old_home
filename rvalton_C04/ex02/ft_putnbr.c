#include <unistd.h>

void	ft_putnbr(int nb)
{
	int r;

	r = 0;
	if (nb != 0)
	{
		r = nb % 10;
		nb = nb / 10;
		ft_putnbr(nb);
		r = r + '0';
		write(1, &r, 1);
	}
}
